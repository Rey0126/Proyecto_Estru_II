    public int cargarAvailList() throws IOException {

        String rrn = "";
        String linea = "";

        try ( RandomAccessFile file = new RandomAccessFile(archivoActual, "rw")) {
            // Leer la metadata de la primera lnea
            String metadata = file.readLine();
            if (metadata == null) {
                System.out.println("El archivo est vaco o no contiene una lnea completa.");
                return 0;
            }

            String[] metaParts = metadata.split("\\|");
            if (metaParts.length < 2) {
                System.out.println("Formato de lnea incorrecto.");
                return 0;
            }

            String cant = metaParts[0].replace("#", ""); // Cantidad de registros
            String cabeza = metaParts[1].replace("#", ""); // Cabeza del AvailList

            if (!cant.isEmpty() && !cabeza.isEmpty()) {
                cantidad = Integer.parseInt(cant);
                cabezaAvail = Integer.parseInt(cabeza);

                // Vaciar el AvailList
                AvailList.clear();
                AvailList.clear();

                int pos = cabezaAvail;
                if (pos != -1) {
                    AvailList.add(pos);
                }
                System.out.println(pos);

                while (pos != -1) {
                    file.seek(pos);

                    // Leer la lnea en la posicin actual
                    linea = file.readLine();
                    if (linea == null) {
                        System.out.println("No se pudo leer la lnea en la posicin: " + pos);
                        break;
                    }

                    // Limpiar el valor de rrn antes de empezar a leer cada lnea
                    rrn = "";
                    boolean dentroAsteriscos = false;

                    // Recorrer los caracteres de la lnea
                    for (char c : linea.toCharArray()) {
                        if (c == '*') {
                            if (dentroAsteriscos) {
                                // Se sale cuando se encuentra el segundo asterisco
                                break;
                            } else {
                                dentroAsteriscos = true;
                            }
                        } else if (dentroAsteriscos) {
                            rrn += c;
                        }
                    }

                    // Convertir el valor de rrn a entero (si contiene un valor numrico)
                    try {
                        pos = Integer.parseInt(rrn); // Convertir a posicin
                    } catch (NumberFormatException e) {
                        pos = -1; // Si no es un nmero vlido, terminamos
                    }

                    // Solo agregar la posicin si es vlida
                    if (pos != -1) {
                        AvailList.add(pos); // Agregar la nueva posicin al AvailList
                    }
                }
            }
        } catch (IOException e) {
            System.out.println("Error al leer el archivo: " + e.getMessage());
            throw e;
        }

        for (Integer integer : AvailList) {
            System.out.println("Nmero avail: " + integer);
        }

        return cantidad;
    }

